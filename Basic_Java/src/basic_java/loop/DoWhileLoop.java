
package basic_java.loop;

public class DoWhileLoop {
    public static void main(String[] args) {
        
        int i = 1;
        do{
            System.out.println(i + " Bangladesh");
            i++;
        }while(i <= 10);
        
    }

    public static class ForLoop {
        public static void main(String[] args) {

            for(int i = 1; i <=10; i = i + 2){
                System.out.println(i + " Bangladesh");
            }

        }
    }
}
